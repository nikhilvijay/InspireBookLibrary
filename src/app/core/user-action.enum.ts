/**
 * Enumeration representing user actions for CRUD operations.
 * - `Save`: Indicates a save action.
 * - `Delete`: Indicates a delete action.
 * - `Update`: Indicates an update action.
 * - `Cancel`: Indicates a cancel action.
 */
export enum UserAction {
    Save = 'Save',
    Delete = 'Delete',
    Update = 'Update',
    Cancel = 'Cancel',
}  